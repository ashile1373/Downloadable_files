package ir.ashilethegreat.payesh.DBhandlers;

public class InquiryModal {

    String planID;
    String planTitle;
    String supervisionName;
    String planStage;
    String planSituation;
    String name;
    String familyName;
    String natCode;
    String phone;
    String inspectorName;
    String inspectorFamilyName;

    public InquiryModal() {
    }

    public String getPlanID() {
        return planID;
    }

    public void setPlanID(String planID) {
        this.planID = planID;
    }

    public String getPlanTitle() {
        return planTitle;
    }

    public void setPlanTitle(String planTitle) {
        this.planTitle = planTitle;
    }

    public String getSupervisionName() {
        return supervisionName;
    }

    public void setSupervisionName(String supervisionName) {
        this.supervisionName = supervisionName;
    }

    public String getPlanStage() {
        return planStage;
    }

    public void setPlanStage(String planStage) {
        this.planStage = planStage;
    }

    public String getPlanSituation() {
        return planSituation;
    }

    public void setPlanSituation(String planSituation) {
        this.planSituation = planSituation;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFamilyName() {
        return familyName;
    }

    public void setFamilyName(String familyName) {
        this.familyName = familyName;
    }

    public String getNatCode() {
        return natCode;
    }

    public void setNatCode(String natCode) {
        this.natCode = natCode;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getInspectorName() {
        return inspectorName;
    }

    public void setInspectorName(String inspectorName) {
        this.inspectorName = inspectorName;
    }

    public String getInspectorFamilyName() {
        return inspectorFamilyName;
    }

    public void setInspectorFamilyName(String inspectorFamilyName) {
        this.inspectorFamilyName = inspectorFamilyName;
    }
}
