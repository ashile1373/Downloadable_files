package ir.ashilethegreat.payesh.DBhandlers;

public class AppInfoModal {
    String appID, appLastVersion, appEssentialVersion, appNewFeatures, appFirstRun, appPattern,
            appPatternEnabled, appFingerPrintEnabled, appTheme, appLogo, appUpdateURL;


    public AppInfoModal() {
    }

    public String getAppID() {
        return appID;
    }

    public void setAppID(String appID) {
        this.appID = appID;
    }

    public String getAppLastVersion() {
        return appLastVersion;
    }

    public void setAppLastVersion(String appLastVersion) {
        this.appLastVersion = appLastVersion;
    }

    public String getAppEssentialVersion() {
        return appEssentialVersion;
    }

    public void setAppEssentialVersion(String appEssentialVersion) {
        this.appEssentialVersion = appEssentialVersion;
    }

    public String getAppNewFeatures() {
        return appNewFeatures;
    }

    public void setAppNewFeatures(String appNewFeatures) {
        this.appNewFeatures = appNewFeatures;
    }

    public String getAppFirstRun() {
        return appFirstRun;
    }

    public void setAppFirstRun(String appFirstRun) {
        this.appFirstRun = appFirstRun;
    }

    public String getAppPattern() {
        return appPattern;
    }

    public void setAppPattern(String appPattern) {
        this.appPattern = appPattern;
    }

    public String getAppPatternEnabled() {
        return appPatternEnabled;
    }

    public void setAppPatternEnabled(String appPatternEnabled) {
        this.appPatternEnabled = appPatternEnabled;
    }

    public String getAppFingerPrintEnabled() {
        return appFingerPrintEnabled;
    }

    public void setAppFingerPrintEnabled(String appFingerPrintEnabled) {
        this.appFingerPrintEnabled = appFingerPrintEnabled;
    }

    public String getAppTheme() {
        return appTheme;
    }

    public void setAppTheme(String appTheme) {
        this.appTheme = appTheme;
    }


    public String getAppLogo() {
        return appLogo;
    }

    public void setAppLogo(String appLogo) {
        this.appLogo = appLogo;
    }

    public String getAppUpdateURL() {
        return appUpdateURL;
    }

    public void setAppUpdateURL(String appUpdateURL) {
        this.appUpdateURL = appUpdateURL;
    }
}
